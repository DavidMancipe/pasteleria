package Modelo;


public class Venta {
    
    Integer id;
    Integer item;
    Integer idcliente;
    Integer idusuario;
    Integer idproducto;
    String codigo;
    String Numserie;
    String Descripcion;
    String fecha;
    double precio;
    Integer cantidad;
    Double subtotal;
    Double monto;
    String estado;

    Cliente cliente;
    
    public Venta() {
    }

    public Venta(String codigo) {
        this.codigo = codigo;
    }

    public Venta(Integer id, Integer item, Integer idcliente, Integer idusuario, Integer idproducto, String codigo, String Numserie, String Descripcion, String fecha, double precio, Integer cantidad, Double subtotal, Double monto, String estado, Cliente cliente) {
        this.id = id;
        this.item = item;
        this.idcliente = idcliente;
        this.idusuario = idusuario;
        this.idproducto = idproducto;
        this.codigo = codigo;
        this.Numserie = Numserie;
        this.Descripcion = Descripcion;
        this.fecha = fecha;
        this.precio = precio;
        this.cantidad = cantidad;
        this.subtotal = subtotal;
        this.monto = monto;
        this.estado = estado;
        this.cliente = cliente;
    }

    

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getItem() {
        return item;
    }

    public void setItem(Integer item) {
        this.item = item;
    }

    public Integer getIdcliente() {
        return idcliente;
    }

    public void setIdcliente(Integer idcliente) {
        this.idcliente = idcliente;
    }

    public Integer getIdusuario() {
        return idusuario;
    }

    public void setIdusuario(Integer idusuario) {
        this.idusuario = idusuario;
    }

    public Integer getIdproducto() {
        return idproducto;
    }

    public void setIdproducto(Integer idproducto) {
        this.idproducto = idproducto;
    }

    public String getNumserie() {
        return Numserie;
    }

    public void setNumserie(String Numserie) {
        this.Numserie = Numserie;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public Integer getCantidad() {
        return cantidad;
    }

    public void setCantidad(Integer cantidad) {
        this.cantidad = cantidad;
    }

    public Double getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(Double subtotal) {
        this.subtotal = subtotal;
    }

    public Double getMonto() {
        return monto;
    }

    public void setMonto(Double monto) {
        this.monto = monto;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public Cliente getCliente() {
        return cliente;
    }

    public void setCliente(Cliente cliente) {
        this.cliente = cliente;
    }
    
    
}
